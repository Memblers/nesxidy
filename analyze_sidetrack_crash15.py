def cpu_to_fixed_offset(cpu_addr):
    """Convert CPU $C000-$FFFF to offset within fixed bank"""
    return cpu_addr - 0xC000

def read_fixed(cpu_addr, length=16):
    off = cpu_to_fixed_offset(cpu_addr)
    return fixed[off:off+length]

