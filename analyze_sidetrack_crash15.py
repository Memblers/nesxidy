def disasm(data, base_addr, count=30):
    i = 0
    lines = 0
    while i < len(data) and lines < count:
        b = data[i]
        cpu = base_addr + i
        if b in OPCODES:
            name, size = OPCODES[b]
            if size == 1:
                print(f"  ${cpu:04X}: {name}")
            elif size == 2:
                if i+1 < len(data):
                    operand = data[i+1]
                    if "rel" in name:
                        target = cpu + 2 + (operand if operand < 128 else operand - 256)
                        print(f"  ${cpu:04X}: {name.split()[0]} ${target:04X}  (${operand:02X})")
                    else:
                        n = name.replace("zp", f"${operand:02X}").replace("imm", f"${operand:02X}")
                        print(f"  ${cpu:04X}: {n}")
                else:
                    print(f"  ${cpu:04X}: {name} ??")
            elif size == 3:
                if i+2 < len(data):
                    lo, hi = data[i+1], data[i+2]
                    addr16 = lo | (hi << 8)
                    n = name.replace("abs", f"${addr16:04X}")
                    print(f"  ${cpu:04X}: {n}")
                else:
                    print(f"  ${cpu:04X}: {name} ????")
            i += size
        else:
            print(f"  ${cpu:04X}: .db ${b:02X}  (unknown opcode)")
            i += 1
        lines += 1

