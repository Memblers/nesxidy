def oplen(op):
    mode = op & 0x1F
    if op in (0x00, 0x08, 0x0A, 0x18, 0x28, 0x2A, 0x38, 0x40,
              0x48, 0x4A, 0x58, 0x60, 0x68, 0x6A, 0x78, 0x88,
              0x8A, 0x98, 0x9A, 0xA8, 0xAA, 0xB8, 0xC8, 0xCA,
              0xD8, 0xE8, 0xEA, 0xF8):
        return 1
    if op in (0x4C, 0x6C, 0x20, 0x2C, 0x8C, 0x8D, 0x8E, 0xAC, 0xAD, 0xAE,
              0xCC, 0xCD, 0xCE, 0xEC, 0xED, 0xEE, 0x99, 0x9D, 0xB9, 0xBD,
              0xBE, 0xD9, 0xDD, 0xDE, 0xF9, 0xFD, 0xFE, 0x19, 0x1D, 0x1E,
              0x39, 0x3D, 0x3E, 0x59, 0x5D, 0x5E, 0x79, 0x7D, 0x7E):
        return 3
    return 2

i = 0
while i < 64:
    addr = 0x2800 + i
    op = rom[i]
    name = names.get(op, f'???${op:02X}')
    length = oplen(op)
    raw = ' '.join(f'{rom[i+j]:02X}' for j in range(length))
    
    if length == 3:
        operand = rom[i+1] | (rom[i+2] << 8)
        print(f'  ${addr:04X}: {raw:10s}  {name} ${operand:04X}')
    elif length == 2:
        operand = rom[i+1]
        if op in (0x10,0x30,0x50,0x70,0x90,0xB0,0xD0,0xF0):  # branch
            import ctypes
            target = addr + 2 + ctypes.c_int8(operand).value
            print(f'  ${addr:04X}: {raw:10s}  {name} ${target:04X}')
        else:
            print(f'  ${addr:04X}: {raw:10s}  {name} ${operand:02X}')
    else:
        print(f'  ${addr:04X}: {raw:10s}  {name}')
    i += length
