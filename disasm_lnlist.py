mnemonics = {}
for op, mn in mn_list:
    mnemonics[op] = mn
def disasm(start, length):
    base_offset = 16 + 31*16384
    pc = start
    end = start + length
    while pc < end:
        off = base_offset + (pc - 0xC000)
        op = data[off]
        l = lens[op]
        mn = mnemonics.get(op, '???(%02X)' % op)
        if l == 1:
            print('  %04X: %02X         %s' % (pc, op, mn))
        elif l == 2:
            b = data[off+1]
            if op in rel:
                target = pc + 2 + (b if b < 128 else b - 256)
                print('  %04X: %02X %02X      %s $%04X' % (pc, op, b, mn, target))
            elif op in imm:
                print('  %04X: %02X %02X      %s #$%02X' % (pc, op, b, mn, b))
            elif op in izx:
                print('  %04X: %02X %02X      %s ($%02X,X)' % (pc, op, b, mn, b))
            elif op in izy:
                print('  %04X: %02X %02X      %s ($%02X),Y' % (pc, op, b, mn, b))
            else:
                print('  %04X: %02X %02X      %s $%02X' % (pc, op, b, mn, b))
        elif l == 3:
            lo = data[off+1]
            hi = data[off+2]
            addr = hi*256+lo
            if op == 0x6C:
                print('  %04X: %02X %02X %02X   %s ($%04X)' % (pc, op, lo, hi, mn, addr))
            elif op in abx:
                print('  %04X: %02X %02X %02X   %s $%04X,X' % (pc, op, lo, hi, mn, addr))
            elif op in aby:
                print('  %04X: %02X %02X %02X   %s $%04X,Y' % (pc, op, lo, hi, mn, addr))
            else:
                print('  %04X: %02X %02X %02X   %s $%04X' % (pc, op, lo, hi, mn, addr))
        pc += l

print('=== lnList handler at $C141 ===')
