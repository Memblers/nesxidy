#!/usr/bin/env python3
"""Disassemble the Asteroids NMI handler from ROM binaries."""

data = bytearray()
data += open('roms/asteroid/035145-04e.ef2','rb').read()  # $6800-$6FFF
data += open('roms/asteroid/035144-04e.h2','rb').read()    # $7000-$77FF
data += open('roms/asteroid/035143-02.j2','rb').read()     # $7800-$7FFF
base = 0x6800

# Vectors
reset = data[0x7FFC - base] | (data[0x7FFD - base] << 8)
nmi   = data[0x7FFA - base] | (data[0x7FFB - base] << 8)
irq   = data[0x7FFE - base] | (data[0x7FFF - base] << 8)
print(f"Reset vector: ${reset:04X}")
print(f"NMI vector:   ${nmi:04X}")
print(f"IRQ vector:   ${irq:04X}")

# 6502 opcode names
opnames = {
    0x00:'BRK',0x01:'ORA (zp,X)',0x05:'ORA zp',0x06:'ASL zp',0x08:'PHP',
    0x09:'ORA #',0x0A:'ASL A',0x0D:'ORA abs',0x0E:'ASL abs',
    0x10:'BPL',0x11:'ORA (zp),Y',0x18:'CLC',0x19:'ORA abs,Y',0x1D:'ORA abs,X',
    0x20:'JSR',0x21:'AND (zp,X)',0x24:'BIT zp',0x25:'AND zp',0x26:'ROL zp',
    0x28:'PLP',0x29:'AND #',0x2A:'ROL A',0x2C:'BIT abs',0x2D:'AND abs',
    0x30:'BMI',0x31:'AND (zp),Y',0x38:'SEC',0x39:'AND abs,Y',0x3D:'AND abs,X',
    0x40:'RTI',0x41:'EOR (zp,X)',0x45:'EOR zp',0x46:'LSR zp',0x48:'PHA',
    0x49:'EOR #',0x4A:'LSR A',0x4C:'JMP',0x4D:'EOR abs',
    0x50:'BVC',0x51:'EOR (zp),Y',0x58:'CLI',0x59:'EOR abs,Y',
    0x60:'RTS',0x61:'ADC (zp,X)',0x65:'ADC zp',0x66:'ROR zp',0x68:'PLA',
    0x69:'ADC #',0x6A:'ROR A',0x6C:'JMP ()',0x6D:'ADC abs',
    0x70:'BVS',0x71:'ADC (zp),Y',0x78:'SEI',0x79:'ADC abs,Y',
    0x81:'STA (zp,X)',0x84:'STY zp',0x85:'STA zp',0x86:'STX zp',0x88:'DEY',
    0x8A:'TXA',0x8C:'STY abs',0x8D:'STA abs',0x8E:'STX abs',
    0x90:'BCC',0x91:'STA (zp),Y',0x94:'STY zp,X',0x95:'STA zp,X',
    0x96:'STX zp,Y',0x98:'TYA',0x99:'STA abs,Y',0x9A:'TXS',0x9D:'STA abs,X',
    0xA0:'LDY #',0xA1:'LDA (zp,X)',0xA2:'LDX #',0xA4:'LDY zp',0xA5:'LDA zp',
    0xA6:'LDX zp',0xA8:'TAY',0xA9:'LDA #',0xAA:'TAX',0xAC:'LDY abs',
    0xAD:'LDA abs',0xAE:'LDX abs',0xB0:'BCS',0xB1:'LDA (zp),Y',
    0xB4:'LDY zp,X',0xB5:'LDA zp,X',0xB6:'LDX zp,Y',0xB9:'LDA abs,Y',
    0xBA:'TSX',0xBD:'LDA abs,X',0xC0:'CPY #',0xC4:'CPY zp',0xC5:'CMP zp',
    0xC6:'DEC zp',0xC8:'INY',0xC9:'CMP #',0xCA:'DEX',0xCC:'CPY abs',
    0xCD:'CMP abs',0xCE:'DEC abs',0xD0:'BNE',0xD5:'CMP zp,X',0xD8:'CLD',
    0xD9:'CMP abs,Y',0xDD:'CMP abs,X',0xDE:'DEC abs,X',
    0xE0:'CPX #',0xE4:'CPX zp',0xE5:'SBC zp',0xE6:'INC zp',0xE8:'INX',
    0xE9:'SBC #',0xEA:'NOP',0xEC:'CPX abs',0xED:'SBC abs',0xEE:'INC abs',
    0xF0:'BEQ',0xF5:'SBC zp,X',0xF6:'INC zp,X',0xF8:'SED',0xFD:'SBC abs,X',
    0xFE:'INC abs,X',
}

# Opcode lengths
op_len = {}
for i in range(256):
    op_len[i] = 1
for op in [0x09,0x29,0x49,0x69,0x89,0xA0,0xA2,0xA9,0xC0,0xC9,0xE0,0xE9,
           0x05,0x06,0x24,0x25,0x26,0x45,0x46,0x65,0x66,0x84,0x85,0x86,
           0xA4,0xA5,0xA6,0xC4,0xC5,0xC6,0xE4,0xE5,0xE6,
           0x94,0x95,0x96,0xB4,0xB5,0xB6,0xD5,0xF5,0xF6,
           0x01,0x11,0x21,0x31,0x41,0x51,0x61,0x71,0x81,0x91,0xA1,0xB1,
           0x10,0x30,0x50,0x70,0x90,0xB0,0xD0,0xF0]:
    op_len[op] = 2
for op in [0x20,0x2C,0x4C,0x6C,0x8C,0x8D,0x8E,0x99,0x9D,0xAC,0xAD,0xAE,
           0xB9,0xBD,0xCC,0xCD,0xCE,0xD9,0xDD,0xDE,0xEC,0xED,0xEE,0xFD,0xFE,
           0x0D,0x0E,0x19,0x1D,0x1E,0x2D,0x2E,0x39,0x3D,0x3E,
           0x4D,0x4E,0x59,0x5D,0x5E,0x6D,0x6E,0x79,0x7D,0x7E]:
    op_len[op] = 3

branch_ops = {0x10,0x30,0x50,0x70,0x90,0xB0,0xD0,0xF0}

def disasm(start_addr, count, label=""):
    if label:
        print(f"\n=== {label} at ${start_addr:04X} ===")
    addr = start_addr
    for _ in range(count):
        off = addr - base
        if off < 0 or off >= len(data):
            print(f"  ${addr:04X}: (out of range)")
            break
        op = data[off]
        n = op_len[op]
        raw = ' '.join(f'{data[off+j]:02X}' for j in range(n))
        name = opnames.get(op, f'???${op:02X}')
        
        if op in branch_ops and n == 2:
            rel = data[off+1]
            if rel >= 128: rel -= 256
            target = addr + 2 + rel
            line = f"  ${addr:04X}: {raw:10s}  {name} ${target:04X}"
            if target == addr:
                line += "  *** SELF-BRANCH ***"
            print(line)
        elif n == 3:
            target = data[off+1] | (data[off+2] << 8)
            print(f"  ${addr:04X}: {raw:10s}  {name} ${target:04X}")
        elif n == 2:
            print(f"  ${addr:04X}: {raw:10s}  {name} ${data[off+1]:02X}")
        else:
            print(f"  ${addr:04X}: {raw:10s}  {name}")
        
        if op in (0x40, 0x60, 0x4C, 0x6C, 0x00):
            print('  ---')
        addr += n

disasm(nmi, 60, "NMI handler")
disasm(reset, 50, "Reset handler")
disasm(0x7A93, 30, "Subroutine $7A93 (called from NMI if $5B < 4)")
